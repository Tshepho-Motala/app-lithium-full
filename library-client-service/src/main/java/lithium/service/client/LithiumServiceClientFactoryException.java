package lithium.service.client;

public class LithiumServiceClientFactoryException extends Exception {

	private static final long serialVersionUID = 1L;

	public LithiumServiceClientFactoryException() {
		super();
	}

	public LithiumServiceClientFactoryException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public LithiumServiceClientFactoryException(String message, Throwable cause) {
		super(message, cause);
	}

	public LithiumServiceClientFactoryException(String message) {
		super(message);
	}

	public LithiumServiceClientFactoryException(Throwable cause) {
		super(cause);
	}

}
